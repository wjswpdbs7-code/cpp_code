// 짝수 합 함수 만들기 설명: 정수 리스트가 주어지면, 리스트에 있는 짝수들의 합을 반환하는 함수를 작성하세요. 
// 입력: 정수 리스트 출력: 정수 (짝수들의 합) 예시: [1,2,3,4,5] → 6 (2+4) 
// 힌트: for로 리스트를 돌면서 if로 짝수인지 검사하고 합에 더하면 됩니다.


//코딩을 위한 사고방식 적용
//1. 문제 이해: 주어진 정수 리스트에서 짝수들의 합을 구하는 함수 작성
//2. 계획 세우기: 리스트를 순회하면서 짝수인지 확인하고 짝수인 경우 합에 더하기

//짝수인것을 구분하기위해서 무엇이 필요한지 
#include <iostream>
using namespace std;

int main(){
    int sum = 0;
    int num[100]; // 정수 리스트를 저장할 배열 (최대 100개)
    int a; // 리스트의 크기

    cin >> a; // 리스트의 크기 입력

    for(int i = 0; i < a; i++){
        cin >> num[i]; // 리스트의 각 요소 입력
    }

    for(int i = 0; i < a; i++){
        if(num[i] % 2 == 0){ // 짝수인지 검사
            sum += num[i]; // 짝수인 경우 합에 더하기
        }
    }

    cout << sum << endl;
}
