#include "a.h"
#include "b.h"

int main(void) {
    foo();
    bar();
    return 0;
}

//3개의 파일이 있습니다. a.c, b.c, main.c입니다. a.c에는 foo() 함수가 정의되어 있고, b.c에는 bar() 함수가 정의되어 있습니다. main.c에서는 foo()와 bar() 함수를 호출하여 각각 "foo"와 "bar"를 출력합니다. 이 코드는 C17 언어 표준을 사용하여 작성되었습니다.

//분할 이유 - 코드의 모듈화: 각 기능을 별도의 파일로 분리하여 코드의 가독성과 유지보수성을 향상시킵니다. 예를 들어, foo() 함수와 bar() 함수를 각각 a.c와 b.c에 정의함으로써, 각 함수의 역할이 명확해지고, 필요한 경우 해당 파일만 수정하면 됩니다.

//분할 이유 - 재사용성: 함수들을 별도의 파일로 분리하면, 다른 프로젝트에서도 해당 함수를 쉽게 재사용할 수 있습니다. 예를 들어, foo() 함수가 다른 프로젝트에서도 필요하다면, a.c와 a.h 파일을 해당 프로젝트에 포함시켜 사용할 수 있습니다.

