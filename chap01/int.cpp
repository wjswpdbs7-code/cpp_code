#include<iostream>

int main() {
    int a = 10; // 정수형 변수 a를 선언하고 10으로 초기화
    char b = 'A'; // 문자형 변수 b를 선언하고 'A'로 초기화
    float c = 3.14f; // 실수형 변수 c를 선언하고 3.14로 초기화

    std::cout << a << std::endl; // 변수 a의 값을 출력
    std::cout << b << std::endl; // 변수 b의 값을 출력
    std::cout << c << std::endl; // 변수 c의 값을 출력
    return 0; // 프로그램 종료
}

