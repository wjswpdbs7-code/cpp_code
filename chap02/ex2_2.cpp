#include<iostream>
using namespace std;

double area(int r) {
    return 3.14 * r * r; // 원의 면적을 계산하는 함수
}
//3번줄을 생략해도 

int main() {
    int n=3; // 원의 반지름을 나타내는 변수 n을 선언하고 3으로 초기화
    char c = '#'; // 문자형 변수 c를 선언하고 '#'로 초기화
    cout << c << 5.5 << '-' << n << "hello" << true << endl; // 다양한 데이터 타입을 출력하는 예시
    cout << "n + 5 = " << n + 5 << '\n'; // n과 5의 합을 출력
    cout << "면적은 " << area(n);
}

//\n과 std::endl의 차이점은 무엇인가요?
//\n은 단순히 줄바꿈을 나타내는 이스케이프 시퀀스입니다. std::endl은 줄바꿈과 버퍼 플러시를 동시에 수행하는 조작자입니다
//std::endl은 출력 버퍼를 비우고 줄바꿈을 추가하는 반면, \n은 줄바꿈만 추가합니다. 
//따라서 std::endl은 출력이 즉시 화면에 나타나도록 보장하지만, \n은 버퍼링이 활성화된 경우 출력이 지연될 수 있습니다.
//일반적으로 std::endl은 출력이 즉시 필요할 때 사용되고, \n은 단순히 줄바꿈이 필요한 경우에 사용됩니다.
//무조건 std::endl을 사용하는 것은 비효율적일 수 있으므로, 필요에 따라 \n과 std::endl을 적절히 선택하여 사용하는 것이 좋습니다.
//return 0; 가 없음에도 불구하고 프로그램이 정상적으로 종료되는 이유는 C++ 표준에서 main 함수가 int를 반환하는 경우, return 0;이 명시적으로 없어도 컴파일러가 자동으로 0을 반환하도록 규정하고 있기 때문입니다. 따라서 main 함수에서 return 0;을 생략해도 프로그램은 정상적으로 종료되고 운영 체제에 0을 반환하게 됩니다. 있어야 하는 경우는 main 함수가 int를 반환하는 경우입니다. void main()과 같이 반환 타입이 void인 경우에는 return 0;이 필요하지 않습니다. 하지만 일반적으로 C++에서는 main 함수는 int를 반환하는 것이 표준이므로, return 0;을 명시적으로 작성하는 것이 좋습니다. 이는 코드의 명확성과 일관성을 높이는 데 도움이 됩니다.
